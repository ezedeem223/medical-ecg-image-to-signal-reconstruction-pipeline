"""
Metadata builder for synthetic ECG benchmark cases.

All cases generated by this module are explicitly labeled synthetic=True.
No real patient data is referenced.
"""
from __future__ import annotations

import json
from pathlib import Path
from typing import Any


SYNTHETIC_LABEL = "SYNTHETIC"
SYNTHETIC_WARNING = (
    "This waveform is parametrically generated. "
    "It does not represent real patient data and must not be used for clinical purposes."
)


def build_case_metadata(
    case_id: str,
    *,
    fs: int,
    num_leads: int,
    signal_length: int,
    heart_rate_bpm: float,
    distortions: list[str],
    generator_params: dict[str, Any],
    seed: int,
) -> dict[str, Any]:
    return {
        "case_id": case_id,
        "synthetic": True,
        "data_source": SYNTHETIC_LABEL,
        "warning": SYNTHETIC_WARNING,
        "signal": {
            "sampling_rate_hz": fs,
            "num_leads": num_leads,
            "signal_length_samples": signal_length,
            "duration_seconds": round(signal_length / fs, 3),
            "heart_rate_bpm": heart_rate_bpm,
            "leads": ["I", "II", "III", "aVR", "aVL", "aVF", "V1", "V2", "V3", "V4", "V5", "V6"][
                :num_leads
            ],
        },
        "distortion_variants": distortions,
        "generator": {
            "module": "tools.synthetic_benchmark.generate_cases",
            "seed": seed,
            "params": generator_params,
        },
    }


def save_metadata(meta: dict[str, Any], path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(meta, indent=2), encoding="utf-8")


def load_metadata(path: Path) -> dict[str, Any]:
    return json.loads(path.read_text(encoding="utf-8"))


def build_manifest(cases: list[dict[str, Any]], output_dir: Path) -> dict[str, Any]:
    return {
        "synthetic": True,
        "warning": SYNTHETIC_WARNING,
        "dataset": "ECG Research Workbench — Synthetic Benchmark Seed",
        "num_cases": len(cases),
        "output_dir": str(output_dir),
        "cases": cases,
    }


def save_manifest(manifest: dict[str, Any], path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(manifest, indent=2), encoding="utf-8")
